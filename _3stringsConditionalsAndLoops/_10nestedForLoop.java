package _3stringsConditionalsAndLoops;

public class _10nestedForLoop {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print(i + " " + j + "\t"); 
            }
            System.out.println(); 
        }
    }
}
