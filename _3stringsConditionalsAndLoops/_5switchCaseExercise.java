package _3stringsConditionalsAndLoops;

public class _5switchCaseExercise {
    public static void main(String[] args) {
        String fruit = "Orange";

        switch (fruit) {
            case "Apple":
                System.out.println("You chose Apple");
                break;
            case "Banana":
                System.out.println("You chose Banana");
                break;
            default:
            System.out.println("Choose Apple or Banana");
        }
    }
}
